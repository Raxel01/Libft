#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*ptr;
	size_t	len ;
	size_t	i;

	len = ft_strlen(ptr) + 1;
	i = 0;
	ptr = (char *)malloc (len * sizeof(char));
	if (!ptr)
		return (NULL);
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
